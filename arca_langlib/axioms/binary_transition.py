# binary_transition.py
# Модуль ArcaLang: Аксиома бинарного перехода
# (0+1)^2 ≡ √2

import math

class BinaryTransition:
    """
    Аксиома бинарного перехода:
    (0+1)^2 ≡ √2
    """

    def __init__(self):
        # фиксируем базовые элементы
        self.zero = 0
        self.one = 1
        self.transition_value = math.sqrt(2)

    def apply(self):
        """
        Возвращает результат аксиомы бинарного перехода.
        """
        return self.transition_value

    def explain(self):
        """
        Философское объяснение аксиомы.
        """
        return (
            "Только конструктивная пара (0+1) формирует и мотивирует абсолютное. "
            "Её операторное возведение в квадрат даёт √2 — минимальную иррациональность, "
            "мост между дискретным и непрерывным."
        )
