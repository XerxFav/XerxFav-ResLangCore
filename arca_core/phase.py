from enum import Enum

QND_CONST = 42  # положительное число

class PhaseBit(Enum):
    """
    Перечисление фазовых состояний в ArcaLang.
    Используется для базовой логики интерпретатора.
    """
    QND = 0       # Квантово‑нейтральное состояние
    PHASE = 1     # Фазовый переход
    ACT = 2       # Активное состояние

# Физическая константа QND (примерная формула)
QND_CONST = (1 / (9.11e-31 * 299_792_458**2)) ** (1/3)

